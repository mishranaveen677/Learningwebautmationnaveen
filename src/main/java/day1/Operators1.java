package day1;

public class Operators1 {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        int sum = x + y;

        System.out.println("sum = " + sum);


        double sub = y - x;
        System.out.println("sub = " + sub);
        sub = x - y;
        System.out.println("sub = " + sub);



    }
}
